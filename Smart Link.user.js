function doiLink (text, matches) {
	var doi = matches[0];
	var org = d['oi' + matches[1]] || 'unknown organization';
	return text.replace(doi, '<a href="http://dx.doi.org/'+doi+'" title="DOI ('+org+')">'+doi+'</a>');
}

function arXivLink (text, matches) {
	return text.replace(matches[0], '<a href="http://arxiv.org/abs/'+matches[1]+'">'+matches[0]+'</a>');
}

function OEISLink(text, matches) {
	return text.replace(matches[0], '<a href="http://oeis.org/'+matches[0]+'">'+matches[0]+'</a>');
}

function isLink(e) {
	while (e.tagName.toUpperCase() != 'HTML') {
		if (e.tagName.toUpperCase() == 'A')
			return true;
		e = e.parentNode;
	}
	return false;
}

function makeTextLinks (xP, reg, func) {
	var allElements = document.evaluate(xP,document,null,XPathResult.UNORDERED_NODE_SNAPSHOT_TYPE,null);
	for (var i = 0; i < allElements.snapshotLength; i++) {
		var el = allElements.snapshotItem(i), e = el;
		if (isLink(el))
			continue;	// Don't override links
		var run = reg.exec(el.innerHTML);
		if (run && run[0])
			el.innerHTML = func(el.innerHTML, run);
	}
}

// The DOI system doesn't say anything useful about the names. Because overlinking would be bad,
// I restricted this in several ways: the prefix (after "10.") must be four or more digits and
// the suffix may not contain spaces, <, >, or ".
var DOI = /\b10\.([0-9]{4,})\/([^\s\<\>\"]+)\b/i;
var arXiv = /\barXiv:\s*([0-9]{4}\.[0-9]{4})/i;

makeTextLinks('//body//span[@class="doi"]', DOI, doiLink);
makeTextLinks('//body//*[contains(text(),"doi:")]', DOI, doiLink);
makeTextLinks('//body//*[contains(text(),"arXiv:")]', arXiv, arXivLink);
